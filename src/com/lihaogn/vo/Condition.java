package com.lihaogn.vo;

public class Condition {
	
	private String foodCookCategory;
	private String foodTypeCategory;
	private String foodName;
	
	public String getFoodCookCategory() {
		return foodCookCategory;
	}
	public void setFoodCookCategory(String foodCookCategory) {
		this.foodCookCategory = foodCookCategory;
	}
	public String getFoodTypeCategory() {
		return foodTypeCategory;
	}
	public void setFoodTypeCategory(String foodTypeCategory) {
		this.foodTypeCategory = foodTypeCategory;
	}
	public String getFoodName() {
		return foodName;
	}
	public void setFoodName(String foodName) {
		this.foodName = foodName;
	}
	
	

}
